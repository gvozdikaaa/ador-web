-- ---------------------------------------------------------------------
-- Таблица подростков-пользователей.
-- Согласно Главе 1.3 пояснительной записки регистрационными сведениями
-- являются фамилия, имя, номер учебной группы, возраст и пол.
-- ---------------------------------------------------------------------
CREATE TABLE users (
    id          INT AUTO_INCREMENT PRIMARY KEY,
    last_name   VARCHAR(64)  NOT NULL,
    first_name  VARCHAR(64)  NOT NULL,
    group_name  VARCHAR(32)  NOT NULL,
    age         TINYINT UNSIGNED NOT NULL,
    gender      ENUM('male', 'female') NOT NULL,
    created_at  DATETIME     NOT NULL DEFAULT CURRENT_TIMESTAMP,
    INDEX idx_group (group_name),
    INDEX idx_full_name (last_name, first_name)
) ENGINE=InnoDB;

-- ---------------------------------------------------------------------
-- Утверждения теста (50 пунктов)
-- ---------------------------------------------------------------------
CREATE TABLE questions (
    id      INT PRIMARY KEY,
    text    TEXT NOT NULL,
    scale   ENUM('positive_interest', 'directiveness', 'hostility',
                 'autonomy', 'inconsistency') NOT NULL
) ENGINE=InnoDB;

-- ---------------------------------------------------------------------
-- Прохождения теста (одно на каждого родителя)
-- ---------------------------------------------------------------------
CREATE TABLE test_sessions (
    id            INT AUTO_INCREMENT PRIMARY KEY,
    user_id       INT NOT NULL,
    parent        ENUM('mother', 'father') NOT NULL,
    raw_positive  TINYINT NOT NULL,
    raw_directive TINYINT NOT NULL,
    raw_hostile   TINYINT NOT NULL,
    raw_autonomy  TINYINT NOT NULL,
    raw_inconsist TINYINT NOT NULL,
    std_positive  TINYINT NOT NULL,
    std_directive TINYINT NOT NULL,
    std_hostile   TINYINT NOT NULL,
    std_autonomy  TINYINT NOT NULL,
    std_inconsist TINYINT NOT NULL,
    completed_at  DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE,
    INDEX idx_user (user_id),
    INDEX idx_completed (completed_at)
) ENGINE=InnoDB;

-- ---------------------------------------------------------------------
-- Детальные ответы (для возможной перепроверки администратором)
-- ---------------------------------------------------------------------
CREATE TABLE answers (
    id          INT AUTO_INCREMENT PRIMARY KEY,
    session_id  INT NOT NULL,
    question_id INT NOT NULL,
    score       TINYINT NOT NULL CHECK (score IN (0, 1, 2)),
    FOREIGN KEY (session_id)  REFERENCES test_sessions(id) ON DELETE CASCADE,
    FOREIGN KEY (question_id) REFERENCES questions(id),
    UNIQUE KEY uk_session_question (session_id, question_id)
) ENGINE=InnoDB;

-- ---------------------------------------------------------------------
-- Настройки, редактируемые администратором через панель.
-- Согласно Главе 1.3 пояснительной записки администратору доступно
-- редактирование текста инструкции, мотивирующего сообщения и
-- настройка электронного адреса педагога-психолога.
-- ---------------------------------------------------------------------
CREATE TABLE settings (
    setting_key   VARCHAR(64) PRIMARY KEY,
    setting_value TEXT NOT NULL,
    description   VARCHAR(255) DEFAULT NULL,
    updated_at    DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP
                  ON UPDATE CURRENT_TIMESTAMP
) ENGINE=InnoDB;

